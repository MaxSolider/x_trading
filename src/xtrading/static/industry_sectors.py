def get_sectors_by_category(category):
    """
    根据分类获取行业板块
    
    Args:
        category (str): 分类名称
        
    Returns:
        list: 该分类下的行业板块列表
    """
    return INDUSTRY_CATEGORIES.get(category, [])

def search_sectors(keyword):
    """
    搜索包含关键词的行业板块
    
    Args:
        keyword (str): 搜索关键词
        
    Returns:
        list: 匹配的行业板块列表
    """
    return [sector for sector in INDUSTRY_SECTORS if keyword in sector]

def get_sector_index(sector_name):
    """
    获取行业板块在列表中的索引
    
    Args:
        sector_name (str): 行业板块名称
        
    Returns:
        int: 索引位置，如果不存在返回-1
    """
    try:
        return INDUSTRY_SECTORS.index(sector_name)
    except ValueError:
        return -1

def get_industry_category(industry_name: str) -> str:
    """
    根据行业名称获取对应的分类
    
    Args:
        industry_name (str): 行业板块名称
        
    Returns:
        str: 行业分类名称
    """
    for category, sectors in INDUSTRY_CATEGORIES.items():
        if industry_name in sectors:
            return category
    return "其他"  # 如果找不到分类，默认为"其他"
